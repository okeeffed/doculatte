class Mathematics {
    /**
     * Multiply the two args
     * 
     * @param {number} a 
     * @param {number} b
     * @memberof Mathematics
     */
    multiply = (a, b) => {
        return a * b;
    }

    /**
     * Add the two args together
     * 
     * @param {number} a 
     * @param {number} b 
     * @memberof Mathematics
     */
    add = (a, b) => {
        return a + b;
    }

    /**
     * Subtract the two args together
     * 
     * @param {number} a 
     * @param {number} b 
     * @memberof Mathematics
     */
    subtract = (a, b) => {
        return a - b;
    }
}